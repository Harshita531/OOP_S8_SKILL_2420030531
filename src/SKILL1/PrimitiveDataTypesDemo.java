package SKILL1;
public class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        
        
        byte age = 25;
        System.out.println("Person's age (byte): " + age);

        
        short currentYear = 2025;
        System.out.println("Current year (short): " + currentYear);

        
        int countryPopulation = 1410000000; 
        System.out.println("Population of a country (int): " + countryPopulation);

       
        long distanceToMoon = 384400000L;  
        System.out.println("Distance from Earth to Moon (long): " + distanceToMoon + " meters");

        
        float pi = 3.14159f;
        System.out.println("Value of Pi (float): " + pi);

        
        double gravity = 9.80665;
        System.out.println("Acceleration due to gravity (double): " + gravity + " m/sÂ²");

       
        char studentGrade = 'A';
        System.out.println("Student's grade (char): " + studentGrade);

        
        boolean isJavaFun = true;
        System.out.println("Is Java fun to learn? (boolean): " + isJavaFun);
    }
}
